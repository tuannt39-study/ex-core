package h17jb3.tu6.Streams;
//XUẤT DỮ LIỆU RA CONSOLE. Chúng ta đã khá quen thuộc với phương thức print() và println(), dùng để xuất dữ liệu ra console. Bên cạnh đó chúng ta cũng có thể dùng phương thức write(). Ví dụ: minh họa sử dụng phương thức system.Out.Write() để xuất ký tự ‘X’ ra console

public class WriteDemo {
	public static void main(String args[]) {
		int b;
		b = 'X';
		System.out.write(b);
		System.out.write('\n');
	}
}
