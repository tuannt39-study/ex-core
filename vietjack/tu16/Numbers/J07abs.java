package vietjack.tu16.Numbers;
//abs() trong Java

public class J07abs {
	public static void main(String args[]) {
		Integer a = -8;
		double d = -100;
		float f = -90;

		// Trả về giá trị tuyệt đối của tham số
		System.out.println(Math.abs(a));
		System.out.println(Math.abs(d));
		System.out.println(Math.abs(f));
	}
}
